public class NumberLiterals {
    public static void main(String[] args) {
        int binary = 0b1010;      // Binary (10 in decimal)
        int octal = 0123;         // Octal (83 in decimal)
        int hex = 0x1A;           // Hexadecimal (26 in decimal)
        float floatNum = 3.14f;   // Floating-point literal
        double doubleNum = 2.718; // Double literal

        System.out.println("Binary: " + binary);
        System.out.println("Octal: " + octal);
        System.out.println("Hexadecimal: " + hex);
        System.out.println("Float: " + floatNum);
        System.out.println("Double: " + doubleNum);
    }
}
